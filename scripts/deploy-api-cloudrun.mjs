#!/usr/bin/env node
/**
 * Deploy services/api to Google Cloud Run (monorepo root as Docker context).
 * Usage: node scripts/deploy-api-cloudrun.mjs
 */
import { readFileSync, writeFileSync, existsSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import { spawnSync } from 'child_process';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

const PROJECT = process.env.GCP_PROJECT_ID || 'autobot-founder';
// asia-south1 = Mumbai, India
const REGION = process.env.GCP_REGION || 'asia-south1';
const SERVICE = process.env.CLOUD_RUN_SERVICE || 'autobot-api';
const IMAGE = `${REGION}-docker.pkg.dev/${PROJECT}/autobot/${SERVICE}:latest`;
const VERCEL_APP = process.env.VERCEL_APP_URL || 'https://autobot-social-plateform-web.vercel.app';
const SKIP_BUILD = process.argv.includes('--skip-build');

function run(cmd, args, opts = {}) {
  console.log(`\n> ${cmd} ${args.join(' ')}`);
  const r = spawnSync(cmd, args, { stdio: 'inherit', cwd: root, ...opts });
  if (r.status !== 0) process.exit(r.status ?? 1);
}

function gcloudOut(args) {
  const r = spawnSync('gcloud', args, { encoding: 'utf8', cwd: root });
  if (r.status !== 0) {
    console.error(r.stderr || 'gcloud failed');
    process.exit(1);
  }
  return r.stdout.trim();
}

function loadEnv(apiPublicUrl) {
  const envPath = resolve(root, 'services/api/.env');
  const saPath = resolve(root, 'firebase-service-account.json');
  if (!existsSync(envPath)) {
    console.error('Missing services/api/.env');
    process.exit(1);
  }
  if (!existsSync(saPath)) {
    console.error('Missing firebase-service-account.json at repo root');
    process.exit(1);
  }

  const vars = {};
  for (const line of readFileSync(envPath, 'utf8').split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq === -1) continue;
    const key = t.slice(0, eq).trim();
    if (key === 'FIREBASE_SERVICE_ACCOUNT_PATH') continue;
    vars[key] = t.slice(eq + 1).trim();
  }

  vars.NODE_ENV = 'production';
  delete vars.PORT; // Cloud Run sets PORT automatically
  delete vars.FIREBASE_SERVICE_ACCOUNT_PATH;
  delete vars.FIREBASE_SERVICE_ACCOUNT_JSON; // use --service-account on Cloud Run instead
  if (apiPublicUrl) {
    vars.API_PUBLIC_URL = apiPublicUrl;
    vars.PUBLIC_URL = VERCEL_APP;
    vars.ALLOWED_ORIGINS = `http://localhost:3000,${VERCEL_APP},${apiPublicUrl}`;
  }
  return vars;
}

function toEnvYaml(vars) {
  const lines = ['# generated by deploy-api-cloudrun.mjs — do not commit'];
  for (const [k, v] of Object.entries(vars)) {
    const escaped = String(v).replace(/\\/g, '\\\\').replace(/"/g, '\\"');
    lines.push(`${k}: "${escaped}"`);
  }
  return lines.join('\n');
}

const envYamlPath = resolve(root, '.env.cloudrun.yaml');

const repoExists =
  spawnSync(
    'gcloud',
    ['artifacts', 'repositories', 'describe', 'autobot', `--location=${REGION}`, `--project=${PROJECT}`],
    { stdio: 'pipe' }
  ).status === 0;

if (!repoExists) {
  run('gcloud', [
    'artifacts', 'repositories', 'create', 'autobot',
    '--repository-format=docker',
    `--location=${REGION}`,
    `--project=${PROJECT}`,
  ]);
}

if (!SKIP_BUILD) {
  run('gcloud', ['auth', 'configure-docker', `${REGION}-docker.pkg.dev`, '--quiet']);
  run('gcloud', [
    'builds', 'submit',
    '--project', PROJECT,
    '--config', 'cloudbuild.api.yaml',
    '--substitutions', `_IMAGE=${IMAGE}`,
    '.',
  ]);
} else {
  console.log('\n⏭  Skipping Docker build (--skip-build). Using existing image:', IMAGE);
}

const saPath = resolve(root, 'firebase-service-account.json');
const serviceAccountEmail = JSON.parse(readFileSync(saPath, 'utf8')).client_email;

writeFileSync(envYamlPath, toEnvYaml(loadEnv()), 'utf8');

run('gcloud', [
  'run', 'deploy', SERVICE,
  '--image', IMAGE,
  '--region', REGION,
  '--project', PROJECT,
  '--platform', 'managed',
  '--allow-unauthenticated',
  '--service-account', serviceAccountEmail,
  '--port', '8080',
  '--memory', '512Mi',
  '--cpu', '1',
  '--min-instances', '0',
  '--max-instances', '10',
  '--timeout', '300',
  '--env-vars-file', envYamlPath,
]);

const apiUrl = gcloudOut([
  'run', 'services', 'describe', SERVICE,
  '--region', REGION,
  '--project', PROJECT,
  '--format', 'value(status.url)',
]);

writeFileSync(envYamlPath, toEnvYaml(loadEnv(apiUrl)), 'utf8');
run('gcloud', [
  'run', 'services', 'update', SERVICE,
  '--region', REGION,
  '--project', PROJECT,
  '--service-account', serviceAccountEmail,
  '--env-vars-file', envYamlPath,
]);

console.log(`\n✅ API: ${apiUrl}`);
console.log(`   Health: ${apiUrl}/health`);
console.log(`\n📌 Vercel env: NEXT_PUBLIC_API_URL=${apiUrl}/api/v1`);
